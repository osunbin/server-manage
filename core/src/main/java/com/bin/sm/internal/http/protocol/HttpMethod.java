package com.bin.sm.internal.http.protocol;

public enum HttpMethod {
    /**
     * All request methods
     */
    ALL,

    /**
     * GET request method
     */
    GET,

    /**
     * POST request method
     */
    POST,

    /**
     * PUT request method
     */
    PUT,

    /**
     * DELETE request method
     */
    DELETE,

    /**
     * PATCH request method
     */
    PATCH,

    /**
     * HEAD request method
     */
    HEAD,

    /**
     * OPTIONS request method
     */
    OPTIONS;
}
